function openNav() {
    document.getElementById("mySidebar").style.width = "250px";
    document.getElementById("opBtn").style.display= "none";
}
  
function closeNav() {
    document.getElementById("mySidebar").style.width = "0";
    document.getElementById("opBtn").style.display= "block";
}


